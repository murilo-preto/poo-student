public class TiposPrimitivos {
    public static void main (String[] args){
        byte b = 10; // 1 byte
        short s = 100; // 2 bytes
        int i = 1000; // 4 bytes
        long l = 10000; // 8 bytes

        float f = 10.0f; // 4 bytes
        double d = 20.0; // 8 bytes

        boolean db = true;
        char c = 'a';
    }
}